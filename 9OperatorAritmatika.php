<?php

// Operator Aritmatika
$a = 10;
$b = 10;
$c = 3;
$d = 2;

// 1. Melakukan Penambahan (+)
$tambah = $a + $b;
echo "Hasil Penjumlahan $a + $b = ";
var_dump($tambah);

// 2. Mencari Sisa Bagi (%)
$modulus = 10 % 3;
echo "Hasil Modulus $a % $c = ";
var_dump($modulus);

// 3. Melakukan Perpangkatan (**)
$pangkat = $d ** $c;
echo "Hasil Pangkat $d ** $c = ";
var_dump($pangkat);